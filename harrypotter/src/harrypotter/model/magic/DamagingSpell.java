package harrypotter.model.magic;

//A spell that damages merpersons, physical obstacles and other players by the given damage amount.

public class DamagingSpell extends Spell {
	private int damageAmount;
	//represents the value that this spell will remove from the target's hp.
	
	public DamagingSpell(String name, int cost, int defaultCoolDown, int damageAmount){
		super(name,cost,defaultCoolDown);
		this.damageAmount=damageAmount;
	}
	
	public int getDamageAmount(){
		return damageAmount;
	}

}
